const arr=['가','나','다','라','마','바','사',]

arr.indexOf('라') //요소찾기 [3]

arr.splice(3,1) //지우기, 3인덱스에서부터 1개 지우기 = '라'만 제거

arr.splice(3,0,'라2') //['가', '나', '다', '라2', '마', '바', '사']



